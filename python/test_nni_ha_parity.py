"""Per-iteration parity test for NNI_ha port (halving=True branch).

Single test case: NNI_ha Q7 (= NNI Q7 same AA + initial_vector, rng(42)).
Loads matlab_ref/nni/nni_ha_reference.mat (generated by generate_nni_ha_reference.m).

Tier design — identical structure to test_nni_parity.py, with three additions:

  GATE — halving-exercised
      Before Tier 1 runs, assert both sides have ``sum(hit_per_outer_history) > 0``.
      If halving never triggered, Tier 1 passes trivially on hit/chit ≡ 0 but
      the halving code path was never exercised — parity test is vacuous for
      NNI_ha. Gate-fail pushes the user to pick a different test case (e.g.
      2020 paper Test_Heig2.m benchmark params).

  Noise-floor truncation (k_star)
      Before Tier 1 runs, scan multiple canary fields (lambda_L, lambda_U,
      res, chit, hit [all ABS], plus y_history [REL]) to find the EARLIEST
      iter where any of them diverges above tol. That iter k_star marks
      entry into the Rayleigh-quotient noise-floor regime (see
      memory/feedback_nni_rayleigh_quotient_noise_floor.md); past that
      point, tiny ``min(x_i)`` amplifies rounding to tol-scale, the
      attained-index of ``max(temp) / min(temp)`` flips between MATLAB and
      Python, halving decisions diverge deterministically, and the two
      paths walk different convergence trajectories despite sharing the
      same exact-arithmetic limit. Tier 1 STRICT is truncated to
      ``[0:k_star)``; iters ``[k_star:K_common)`` are demoted to Tier 3
      INFO. Multi-canary scan (vs single y_history canary) catches the
      case where lambda_L diverges at iter k but y_history waits until
      iter k+1 — y is computed at the START of iter k using iter k-1's
      clean x, so attained-index lottery in iter k's END flips lambda_L
      but not y. This matches §九 9.4 risk 1/4 in nni_hazard_analysis.md:
      halving=True amplifies the stopping-iter lottery into a full-path
      lottery.

  Tier 1 STRICT — overlap slots [0:k_star]
      - ABS tol 1e-10:
          lambda_U_history, lambda_L_history, res_history (1-D)
          x_history, w_history (2-D)
          chit_history, hit_per_outer_history (INT — should be bit-identical
          between MATLAB and Python since halving `while` loop is verbatim
          mirrored; any diff indicates a real port bug)
      - REL tol 1e-10:
          y_history[:, 1:K_common]  (linear solve output; slot 0 is init NaN)

  Tier 2 APPROX — final outputs (algorithmic equivalence)
      - lambda_U: abs diff ≤ 1e-12
      - x:        rel diff ≤ 1e-8

  Tier 3 INFORMATIONAL (no assert)
      - nit scalars (MATLAB 1-based vs Python+1)
      - chit totals (both sides should match since int bit-identical, but
        log for visual confirmation)
      - Non-monotone λ_U transitions (halving break → §九 9.4 caveat)
      - MATLAB's extra iters if any (same noise-floor oscillation as canonical)

PASS condition: GATE + Tier 1 (over [0:k_star)) + Tier 2 全過.

Why add the gate:
  Phase 1 §9.4 risk 1 flagged the concern that a diagonally-dominant Q7 M-tensor
  might not overshoot the full step, leaving halving untriggered. Phase 2
  empirically showed MATLAB Q7 triggers halving 109 times (Python 19 times on
  MATLAB-loaded AA). Gate asserts both > 0 so refactors that accidentally
  disable halving are caught.

Why the noise-floor truncation:
  Empirical Q7 parity run (2026-04-24) showed iter 0-29 bit-identical, iter
  30 diverges at y_history REL=1.19e-10 / lambda_L diff=3.7e-4, then both
  branches reach the same eigenvalue by their respective stopping iters
  (MATLAB=59, Python=32; final |Δλ|=3e-14, Tier 2 PASS). The [30:32) overlap
  fails Tier 1 STRICT but is noise-floor artifact, not a port bug. See
  nni_hazard_analysis.md §9.6 for the full empirical record.
"""
from pathlib import Path

import numpy as np
from scipy.io import loadmat
from scipy.sparse import issparse

from poc_iteration import find_divergence, print_neighborhood, report
from tensor_utils import nni

REF_DIR = Path(__file__).resolve().parent.parent / "matlab_ref" / "nni"

# Tier thresholds (match canonical test_nni_parity.py — halving path uses same
# numerical primitives as full-step path, no reason to loosen)
TIER1_ABS = 1e-10            # Tier 1 STRICT ABS — per-iter history overlap slots
TIER1_Y_RTOL = 1e-10         # Tier 1 STRICT REL — y_history (linear solve output)
TIER2_LAMBDA_ABS = 1e-12     # Tier 2 APPROX — final lambda
TIER2_X_RTOL = 1e-8          # Tier 2 APPROX — final x (eigenvector rel diff)


def _per_iter_max_abs_diff(ml_2d, py_2d):
    diff = ml_2d - py_2d
    diff = np.where(np.isnan(diff), 0.0, diff)
    return np.max(np.abs(diff), axis=0)


def _per_iter_max_magnitude(ml_2d):
    ml_safe = np.where(np.isnan(ml_2d), 0.0, ml_2d)
    return np.max(np.abs(ml_safe), axis=0)


def _compare_2d(ml_2d, py_2d, tolerance, name):
    per_iter = _per_iter_max_abs_diff(ml_2d, py_2d)
    return find_divergence(per_iter, np.zeros_like(per_iter), tolerance, name)


def _compare_2d_relative(ml_2d, py_2d, rtol, name):
    per_iter_abs = _per_iter_max_abs_diff(ml_2d, py_2d)
    per_iter_mag = _per_iter_max_magnitude(ml_2d)
    per_iter_rel = per_iter_abs / np.maximum(per_iter_mag, 1e-300)
    return find_divergence(per_iter_rel, np.zeros_like(per_iter_rel), rtol, name)


def _print_tier_header(K_ml, K_py, K_common, k_star):
    print(
        f"\nTier semantics (K_ml={K_ml}, K_py={K_py}, K_common={K_common}, k_star={k_star}):\n"
        f"  GATE — halving must be exercised: sum(hit) > 0 on both sides\n"
        f"  Tier 1 STRICT — overlap slots [0:{k_star})   [truncated at noise-floor divergence]:\n"
        f"    ABS tol {TIER1_ABS:.0e}:\n"
        f"      - x_history, w_history                              (2-D)\n"
        f"      - lambda_U_history, lambda_L_history, res_history   (1-D)\n"
        f"      - chit_history, hit_per_outer_history                (INT, must bit-match)\n"
        f"    REL tol {TIER1_Y_RTOL:.0e}:\n"
        f"      - y_history[:, 1:{k_star}]  (linear solve output, slot 0 NaN init)\n"
        f"  Tier 2 APPROX — final outputs (algorithmic equivalence):\n"
        f"      - final lambda_U  ABS tol {TIER2_LAMBDA_ABS:.0e}\n"
        f"      - final x         REL tol {TIER2_X_RTOL:.0e}\n"
        f"  Tier 3 INFORMATIONAL (no-assert):\n"
        f"      - nit scalars, chit totals, non-monotone λ_U transitions, extra iters\n"
        f"      - post-truncation overlap [{k_star}:{K_common}) diffs (noise-floor regime)\n"
        f"  PASS = GATE + Tier 1 (over [0:{k_star})) + Tier 2 全過; Tier 3 prints info only."
    )


def test_nni_ha_parity_spsolve():
    ref_path = REF_DIR / "nni_ha_reference.mat"
    if not ref_path.exists():
        raise FileNotFoundError(
            f"{ref_path} not found. Run matlab_ref/nni/generate_nni_ha_reference.m "
            "in MATLAB first."
        )

    data = loadmat(str(ref_path), squeeze_me=True)

    # --- Inputs ---
    AA = data["AA"]
    m = int(data["m"])
    tol = float(data["tol"])
    maxit = int(data["maxit"])
    initial_vector = np.asarray(data["initial_vector"], dtype=np.float64).ravel()

    n = AA.shape[0]
    print(
        f"Loaded nni_ha_reference.mat: "
        f"AA {'sparse' if issparse(AA) else 'dense'} {AA.shape} "
        f"nnz={AA.nnz if issparse(AA) else '-'}, "
        f"m={m}, tol={tol:.0e}, maxit={maxit}"
    )

    # --- MATLAB outputs ---
    x_ml        = np.asarray(data["x"], dtype=np.float64).ravel()
    lambda_ml   = float(data["lambda"])
    lambda_L_ml = float(data["lambda_L"])
    res_ml      = np.asarray(data["res"], dtype=np.float64).ravel()
    nit_ml      = int(data["nit"])
    chit_ml     = int(data["chit"])

    x_history_ml             = np.asarray(data["x_history"], dtype=np.float64)
    lambda_U_history_ml      = np.asarray(data["lambda_U_history"], dtype=np.float64).ravel()
    lambda_L_history_ml      = np.asarray(data["lambda_L_history"], dtype=np.float64).ravel()
    w_history_ml             = np.asarray(data["w_history"], dtype=np.float64)
    y_history_ml             = np.asarray(data["y_history"], dtype=np.float64)
    chit_history_ml          = np.asarray(data["chit_history"], dtype=np.float64).ravel()
    hit_per_outer_history_ml = np.asarray(data["hit_per_outer_history"], dtype=np.float64).ravel()

    print(f"MATLAB: nit={nit_ml} (1-based → K_ml = nit = {nit_ml})")
    print(f"        chit={chit_ml} (halving active; expected > 0)")
    print(f"        lambda_U={lambda_ml:.15e}")
    print(f"        lambda_L={lambda_L_ml:.15e}")

    # --- Run Python NNI with halving=True, record_history=True, spsolve branch ---
    lambda_py, x_py, nit_py, lambda_L_py, res_py, lambda_U_hist_py, history_py = nni(
        AA, m, tol,
        linear_solver="spsolve",
        halving=True,
        tol_theta=1e-12,
        initial_vector=initial_vector,
        maxit=maxit,
        record_history=True,
        matlab_compat=True,
    )
    print(
        f"Python: nit={nit_py} (0-based → K_py = nit+1 = {nit_py + 1}), "
        f"lambda_U={lambda_py:.15e}, lambda_L={lambda_L_py:.15e}"
    )

    K_ml = nit_ml
    K_py = nit_py + 1
    K_common = min(K_ml, K_py)

    # --- Shape sanity ---
    shape_checks = [
        (x_history_ml, history_py["x_history"], n, "x_history"),
        (lambda_U_history_ml, history_py["lambda_U_history"], None, "lambda_U_history"),
        (lambda_L_history_ml, history_py["lambda_L_history"], None, "lambda_L_history"),
        (res_ml, res_py, None, "res_history"),
        (w_history_ml, history_py["w_history"], n, "w_history"),
        (y_history_ml, history_py["y_history"], n, "y_history"),
        (chit_history_ml, history_py["chit_history"], None, "chit_history"),
        (hit_per_outer_history_ml, history_py["hit_per_outer_history"], None, "hit_per_outer_history"),
    ]
    for ml_arr, py_arr, leading_n, name in shape_checks:
        if leading_n is not None:
            expected_ml = (leading_n, K_ml)
            expected_py = (leading_n, K_py)
        else:
            expected_ml = (K_ml,)
            expected_py = (K_py,)
        if ml_arr.shape != expected_ml:
            raise AssertionError(
                f"{name} MATLAB shape {ml_arr.shape} != expected {expected_ml}"
            )
        if py_arr.shape != expected_py:
            raise AssertionError(
                f"{name} Python shape {py_arr.shape} != expected {expected_py}"
            )

    # ============================================================
    # GATE: halving must have been exercised
    # ============================================================
    print(f"\n=== GATE — halving-exercised check ===")
    hit_sum_ml = int(np.sum(hit_per_outer_history_ml))
    hit_sum_py = int(np.sum(history_py["hit_per_outer_history"]))
    print(f"  sum(hit_per_outer_history) MATLAB = {hit_sum_ml}")
    print(f"  sum(hit_per_outer_history) Python = {hit_sum_py}")
    if hit_sum_ml == 0 and hit_sum_py == 0:
        raise AssertionError(
            "GATE FAIL: halving never triggered on either side — this Q7 case "
            "does not exercise the halving `while` loop, so NNI_ha parity is "
            "vacuous. Use a different test case (e.g. Test_Heig2.m benchmark)."
        )
    if hit_sum_ml == 0 or hit_sum_py == 0:
        raise AssertionError(
            f"GATE FAIL: halving triggered on ONE side only (ml={hit_sum_ml}, "
            f"py={hit_sum_py}) — indicates a real port divergence, not a test "
            f"case problem. Investigate step_length halving branch."
        )
    print(f"  ✓ halving exercised on both sides")

    # ============================================================
    # Noise-floor truncation — find first iter where ANY canary field
    # diverges (min over multiple canaries). k_star = that iter; Tier 1
    # STRICT is restricted to [0:k_star). See module docstring for rationale.
    #
    # Why multi-canary: y_history (REL, linear solve output) lags by one
    # outer iter — it is computed at the START of iter k using x from the
    # END of iter k-1, so when min(temp) attained-index lottery flips at
    # iter k's end, lambda_L_history[k] diverges but y_history[k] is still
    # bit-identical (it used iter k-1's clean x). Canaries below cover all
    # the ways noise-floor divergence can first manifest.
    # ============================================================
    print(f"\n=== Noise-floor truncation check ===")
    canaries = []
    # ABS canaries (1-D float)
    for ml, py, name in [
        (lambda_L_history_ml, history_py["lambda_L_history"], "lambda_L_history"),
        (lambda_U_history_ml, history_py["lambda_U_history"], "lambda_U_history"),
        (res_ml, res_py, "res_history"),
    ]:
        diff = np.abs(ml[:K_common] - py[:K_common])
        over = np.where(diff > TIER1_ABS)[0]
        if len(over) > 0:
            canaries.append((name, int(over[0]), float(diff[int(over[0])])))
    # INT canaries — any non-zero diff is divergence (hit/chit must bit-match)
    for ml, py, name in [
        (chit_history_ml, history_py["chit_history"], "chit_history"),
        (hit_per_outer_history_ml, history_py["hit_per_outer_history"], "hit_per_outer_history"),
    ]:
        diff = np.abs(ml[:K_common] - py[:K_common])
        over = np.where(diff > 0)[0]
        if len(over) > 0:
            canaries.append((name, int(over[0]), float(diff[int(over[0])])))
    # REL canary (2-D, y_history; slot 0 is NaN init)
    y_per_iter_abs_full = _per_iter_max_abs_diff(
        y_history_ml[:, :K_common], history_py["y_history"][:, :K_common]
    )
    y_per_iter_mag_full = _per_iter_max_magnitude(y_history_ml[:, :K_common])
    y_per_iter_rel_full = y_per_iter_abs_full / np.maximum(y_per_iter_mag_full, 1e-300)
    over = np.where(y_per_iter_rel_full[1:] > TIER1_Y_RTOL)[0]
    if len(over) > 0:
        k = int(over[0]) + 1  # +1 offset because scan started at slot 1
        canaries.append(("y_history (REL)", k, float(y_per_iter_rel_full[k])))

    if not canaries:
        k_star = K_common
        print(
            f"  all canaries within tol for {K_common} overlap slots\n"
            f"  → no noise-floor divergence detected; Tier 1 overlap = [0:{K_common})"
        )
    else:
        k_star = min(iter_k for _, iter_k, _ in canaries)
        triggers = [c for c in canaries if c[1] == k_star]
        print(f"  canary scan (abs tol {TIER1_ABS:.0e}, y rel tol {TIER1_Y_RTOL:.0e}):")
        for name, iter_k, diff_val in sorted(canaries, key=lambda c: c[1]):
            marker = "  <-- EARLIEST" if iter_k == k_star else ""
            print(f"    {name:30s} first diverges at iter {iter_k:3d}  diff={diff_val:.3e}{marker}")
        print(
            f"  → Noise floor divergence at iter {k_star} "
            f"(triggered by: {', '.join(t[0] for t in triggers)})"
        )
        print(
            f"  → Tier 1 STRICT truncated: [0:{k_star})  "
            f"(K_common={K_common}, demoting [{k_star}:{K_common}) to Tier 3 INFO)"
        )

    _print_tier_header(K_ml, K_py, K_common, k_star)

    # ============================================================
    # Tier 1: STRICT — over [0:k_star)
    # ============================================================
    print(f"\n=== Tier 1 STRICT (overlap [0:{k_star})) ===")
    tier1_fails = []

    # 1a: 1-D fields, ABS
    for ml, py, name in [
        (lambda_U_history_ml, history_py["lambda_U_history"], "lambda_U_history"),
        (lambda_L_history_ml, history_py["lambda_L_history"], "lambda_L_history"),
        (res_ml, res_py, "res_history"),
        (chit_history_ml, history_py["chit_history"], "chit_history"),
        (hit_per_outer_history_ml, history_py["hit_per_outer_history"], "hit_per_outer_history"),
    ]:
        r = find_divergence(ml[:k_star], py[:k_star], TIER1_ABS, name)
        report(r)
        if not r["passed"]:
            tier1_fails.append((name, r, ml, py))

    # 1b: 2-D fields, ABS
    for ml_2d, py_2d, name in [
        (x_history_ml, history_py["x_history"], "x_history (2D)"),
        (w_history_ml, history_py["w_history"], "w_history (2D)"),
    ]:
        r = _compare_2d(ml_2d[:, :k_star], py_2d[:, :k_star], TIER1_ABS, name)
        report(r)
        if not r["passed"]:
            tier1_fails.append((name, r, None, None))

    # 1c: y_history REL, slots [1:k_star]
    if k_star >= 2:
        r = _compare_2d_relative(
            y_history_ml[:, 1:k_star],
            history_py["y_history"][:, 1:k_star],
            TIER1_Y_RTOL,
            f"y_history[:, 1:{k_star}] (REL)",
        )
        report(r)
        if not r["passed"]:
            tier1_fails.append(("y_history", r, None, None))

    # ============================================================
    # Tier 2: APPROX
    # ============================================================
    print(f"\n=== Tier 2 APPROX — final outputs ===")
    tier2_fails = []

    lambda_diff = abs(lambda_ml - lambda_py)
    lambda_pass = lambda_diff <= TIER2_LAMBDA_ABS
    print(
        f"  [final lambda_U] {'PASS' if lambda_pass else 'FAIL'}  "
        f"|λ_ml - λ_py| = {lambda_diff:.3e}  (tol {TIER2_LAMBDA_ABS:.0e})"
    )
    if not lambda_pass:
        tier2_fails.append("final lambda_U")

    x_diff_abs = float(np.linalg.norm(x_ml - x_py))
    x_norm_ml = float(np.linalg.norm(x_ml))
    x_rel = x_diff_abs / max(x_norm_ml, 1e-300)
    x_pass = x_rel <= TIER2_X_RTOL
    print(
        f"  [final x]        {'PASS' if x_pass else 'FAIL'}  "
        f"||x_ml - x_py|| / ||x_ml|| = {x_rel:.3e}  (rtol {TIER2_X_RTOL:.0e})"
    )
    if not x_pass:
        tier2_fails.append("final x")

    # ============================================================
    # Tier 3: INFORMATIONAL
    # ============================================================
    print(f"\n=== Tier 3 INFORMATIONAL (no-assert) ===")
    nit_match = (nit_ml == nit_py + 1)
    print(
        f"  nit: MATLAB={nit_ml}, Python+1={nit_py + 1}  "
        f"{'MATCH' if nit_match else 'DIFF (halving-break lottery or noise floor)'}"
    )
    print(
        f"  chit (total halving): MATLAB={chit_ml}, Python={hit_sum_py}  "
        f"{'MATCH' if chit_ml == hit_sum_py else 'DIFF (bit-identical expected; check Tier 1 chit_history)'}"
    )

    # Non-monotone λ_U transitions (halving break → §九 9.4 caveat)
    ml_mono_breaks = int(np.sum(
        np.diff(lambda_U_history_ml[:K_common]) > 1e-13
    ))
    py_mono_breaks = int(np.sum(
        np.diff(history_py["lambda_U_history"][:K_common]) > 1e-13
    ))
    print(
        f"  non-monotone λ_U transitions: MATLAB={ml_mono_breaks}, Python={py_mono_breaks}  "
        f"(halving break returns non-monotone λ_U, per §九 9.4 — informational)"
    )

    # Post-truncation overlap diffs (demoted from Tier 1 STRICT to INFO)
    if k_star < K_common:
        print(
            f"\n  Post-truncation overlap [{k_star}:{K_common}) — Tier 1 STRICT demoted to INFO\n"
            f"  (noise-floor regime; expected divergence, Tier 2 PASS confirms both sides\n"
            f"   converge to same eigenvalue):"
        )
        for ml, py, name in [
            (lambda_U_history_ml, history_py["lambda_U_history"], "lambda_U_history"),
            (lambda_L_history_ml, history_py["lambda_L_history"], "lambda_L_history"),
            (res_ml, res_py, "res_history"),
            (chit_history_ml, history_py["chit_history"], "chit_history"),
            (hit_per_outer_history_ml, history_py["hit_per_outer_history"], "hit_per_outer_history"),
        ]:
            diffs = np.abs(ml[k_star:K_common] - py[k_star:K_common])
            max_d = float(diffs.max()) if len(diffs) > 0 else 0.0
            print(f"    {name}: max abs diff = {max_d:.3e}")
        for ml_2d, py_2d, name in [
            (x_history_ml, history_py["x_history"], "x_history (2D)"),
            (w_history_ml, history_py["w_history"], "w_history (2D)"),
        ]:
            per_iter_diffs = _per_iter_max_abs_diff(
                ml_2d[:, k_star:K_common], py_2d[:, k_star:K_common]
            )
            max_d = float(per_iter_diffs.max()) if len(per_iter_diffs) > 0 else 0.0
            print(f"    {name}: max abs diff = {max_d:.3e}")

    if K_ml > K_common:
        print(
            f"\n  MATLAB 多出 {K_ml - K_common} iter (Python 已於 iter {K_common - 1} 停止):"
        )
        for k in range(K_common, K_ml):
            print(
                f"    iter {k}: λ_U={lambda_U_history_ml[k]:.15e}  "
                f"res={res_ml[k]:.3e}  λ_L={lambda_L_history_ml[k]:.15e}"
            )
    elif K_py > K_common:
        print(f"\n  Python 多出 {K_py - K_common} iter (MATLAB 已停): unusual; investigate.")

    # ============================================================
    # Final pass/fail
    # ============================================================
    if tier1_fails or tier2_fails:
        print(f"\n=== FAIL ===")
        print(f"  Tier 1 failures: {len(tier1_fails)}")
        print(f"  Tier 2 failures: {len(tier2_fails)}")
        for name, r, ml_full, py_full in tier1_fails:
            if isinstance(r, dict) and r.get("first_bad_iter") is not None and ml_full is not None:
                print_neighborhood(ml_full, py_full, r["first_bad_iter"], 2, name)
        raise AssertionError("NNI_ha parity FAILED at Tier 1 or Tier 2")

    print(f"\n=== PASS ===")
    print(
        f"  GATE         : halving exercised (ml={hit_sum_ml}, py={hit_sum_py})"
    )
    if k_star == K_common:
        print(
            f"  Tier 1 STRICT: all {K_common} overlap slots within abs {TIER1_ABS:.0e} "
            f"/ y_history within rel {TIER1_Y_RTOL:.0e} (no noise-floor truncation)"
        )
    else:
        print(
            f"  Tier 1 STRICT: all {k_star} pre-noise-floor slots within abs {TIER1_ABS:.0e} "
            f"/ y_history within rel {TIER1_Y_RTOL:.0e}\n"
            f"                 [{k_star}:{K_common}) demoted to Tier 3 INFO"
        )
    print(
        f"  Tier 2 APPROX: final λ abs {lambda_diff:.3e} ≤ {TIER2_LAMBDA_ABS:.0e}; "
        f"final x rel {x_rel:.3e} ≤ {TIER2_X_RTOL:.0e}"
    )
    if K_ml > K_common or K_py > K_common or k_star < K_common:
        print(
            f"  Tier 3 INFO  : stopping iter / post-truncation diffs documented above"
        )


